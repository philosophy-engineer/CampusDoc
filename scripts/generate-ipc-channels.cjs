const fs = require("node:fs");
const path = require("node:path");

const projectRoot = path.resolve(__dirname, "..");
const sourcePath = path.join(projectRoot, "electron", "shared", "ipc-channels.source.json");
const sharedOutputPath = path.join(projectRoot, "electron", "shared", "ipc-channels.js");
const preloadPath = path.join(projectRoot, "electron", "preload.js");

function readSourceChannels() {
  const raw = fs.readFileSync(sourcePath, "utf8");
  const parsed = JSON.parse(raw);
  if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) {
    throw new Error("ipc-channels.source.json must be an object.");
  }

  const entries = Object.entries(parsed);
  if (entries.length === 0) {
    throw new Error("ipc-channels.source.json must not be empty.");
  }

  const values = new Set();
  for (const [key, value] of entries) {
    if (!/^[A-Za-z][A-Za-z0-9_]*$/.test(key)) {
      throw new Error(`Invalid channel key: ${key}`);
    }
    if (typeof value !== "string" || value.length === 0) {
      throw new Error(`Invalid channel value for key: ${key}`);
    }
    if (values.has(value)) {
      throw new Error(`Duplicate channel value: ${value}`);
    }
    values.add(value);
  }

  return parsed;
}

function buildChannelsLiteral(channels) {
  const lines = Object.entries(channels).map(([key, value]) => `  ${key}: ${JSON.stringify(value)},`);
  return `const IPC_CHANNELS = {\n${lines.join("\n")}\n};`;
}

function writeSharedModule(channelsLiteral) {
  const content = `${channelsLiteral}\n\nmodule.exports = {\n  IPC_CHANNELS,\n};\n`;
  fs.writeFileSync(sharedOutputPath, content, "utf8");
}

function updatePreload(channelsLiteral) {
  const startMarker = "// <IPC_CHANNELS:START>";
  const endMarker = "// <IPC_CHANNELS:END>";
  const preloadRaw = fs.readFileSync(preloadPath, "utf8");
  const pattern = new RegExp(`${startMarker}[\\s\\S]*?${endMarker}`);

  if (!pattern.test(preloadRaw)) {
    throw new Error("preload.js does not contain IPC channel markers.");
  }

  const generatedBlock = [
    startMarker,
    "// This block is generated by scripts/generate-ipc-channels.cjs. Do not edit manually.",
    channelsLiteral,
    endMarker,
  ].join("\n");

  const nextPreload = preloadRaw.replace(pattern, generatedBlock);
  fs.writeFileSync(preloadPath, nextPreload, "utf8");
}

function main() {
  const channels = readSourceChannels();
  const channelsLiteral = buildChannelsLiteral(channels);
  writeSharedModule(channelsLiteral);
  updatePreload(channelsLiteral);
  process.stdout.write("Generated IPC channels for main/preload.\n");
}

main();
